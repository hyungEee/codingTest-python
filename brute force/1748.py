import sys

n=sys.stdin.readline().rstrip()
d=len(n) # n의 자릿수
n=int(n)
ans=0

for i in range(1,d):
    ans+=i*9*10**(i-1)
ans+=(n-10**(d-1)+1)*d
print(ans)

# 1~9:		 +1 => n//10 <1			   1*9*10^0
# 10~99:	 +2 => 1<= n//10 <10	   2*9*10^1
# 100~999:	 +3 => 10<= n//10 <100     3*9*10^2
# 1000~9999: +4 => 100<= n//10 < 1000  4*9*10^3
# 								       d*9*10^(d-1)
# 새로 만들어진 수: 1*9 + 2*9*10 + ...(n-1회 반복) + (n-10^(d-1)+1)*d
