
n=int(input())

dp=[[0,0] for _ in range(n+1)] #dp[n][0], dp[n][1]은 각각 0,1로 끝나는 이친수 갯수
dp[1][1]=1

# 1
# 1+0
# 10+0, 10+1
# 100+0, 100+1, 101+0
# 1000+0, 1000+1, 1001+0, 1010+0, 1010+1
# 0으로 끝나면 0,1 붙일 수 있고, 1로 끝나면 0만 붙일 수 있는 규칙이 있음
# dp[n][0] = dp[n-1][0]+dp[n-1][1]
# dp[n][1] = dp[n-1][0]

for i in range(2,n+1):
    dp[i][0]=dp[i-1][0]+dp[i-1][1]
    dp[i][1]=dp[i-1][0]

print(dp[n][0]+dp[n][1])